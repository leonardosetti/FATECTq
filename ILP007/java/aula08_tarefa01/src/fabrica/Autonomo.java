package fabrica;

public class Autonomo extends Equipamento {
	
	private String modelo; 
	private String ia; 
	private String base;
	
	public String getModelo() {
		return modelo;
	}
	public String getIa() {
		return ia;
	}
	public String getBase() {
		return base;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public void setIa(String ia) {
		this.ia = ia;
	}
	public void setBase(String base) {
		this.base = base;
	}

}
