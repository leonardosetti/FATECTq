
public class Pecas {

	private long numero;
	private String nome;
	private double preco;

	public long getNumero() {
		return numero;
	}

	public String getNome() {
		return nome;
	}

	public double getPreco() {
		return preco;
	}

	public void setNumero(long numero) {
		this.numero = numero;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public void setPreco(double preco) {
		this.preco = preco;
	}

}
