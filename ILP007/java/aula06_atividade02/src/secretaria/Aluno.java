package secretaria;

public class Aluno {
	
	private long ra;
	private String nome;
	private double enem;
	
	
	public long getRa() {
		return ra;
	}
	public void setRa(long ra) {
		this.ra = ra;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getEnem() {
		return enem;
	}
	public void setEnem(double enem) {
		this.enem = enem;
	}

}
