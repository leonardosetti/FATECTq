
public abstract class Documento {

	private long numero;
	private String data;
	public long getNumero() {
		return numero;
	}
	public String getData() {
		return data;
	}
	public void setNumero(long numero) {
		this.numero = numero;
	}
	public void setData(String data) {
		this.data = data;
	}
	
	
}
