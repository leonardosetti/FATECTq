
public class Email {
	private String nome;
	private String senha;
	private Login[] login;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSenha() {
		return senha;
	}

	public void setSenha(String senha) {
		this.senha = senha;
	}

	public Login[] getLogin() {
		return login;
	}

	public void setLogin(Login[] login) {
		this.login = login;
	}

}
