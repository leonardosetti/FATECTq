package historico;

public class Relatorio {

	private String ordem;
	private String texto;

	public String getOrdem() {
		return ordem;
	}

	public String getTexto() {
		return texto;
	}

	public void setOrdem(String ordem) {
		this.ordem = ordem;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}

}
