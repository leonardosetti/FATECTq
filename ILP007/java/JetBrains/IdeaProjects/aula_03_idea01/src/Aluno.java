public class Aluno {
    private long ra;
    private String nome;
    private String nascimento;

    public void setRa(long ra) {
        this.ra = ra;
    }
    public long getRa() {
        return ra;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getNome() {
        return nome;
    }
    public void setNascimento(String nascimento) {
        this.nascimento = nascimento;
    }
    public String getNascimento() {
        return nascimento;
    }
}